<template>
  <div id="app">
    <NavBar/>
    <router-view/>
  </div>
</template>

<script>
import NavBar from './components/NavBar'

export default {
  name: 'App',
  components: {
    NavBar
  },
  created () {
    const token = localStorage.getItem('access_token')
    if (token) {
      this.$store.commit('loggedIn', true)
    } else {
      this.$store.commit('loggedIn', false)
      this.$router.push({ name: 'Login' })
    }
  }
}
</script>

